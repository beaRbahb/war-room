/**
 * Get odds info for a slot.
 */
export function getSlotOdds(slot: number): SlotOdds | undefined {
  return SLOT_ODDS[slot];
}

/**
 * Get ESPN pick probability for a player at a given slot.
 * Returns 0 if no data exists.
 */
export function getPickProb(slot: number, playerName: string): number {
  return PICK_PROBS[slot]?.[playerName] ?? 0;
}

/**
 * Get the consensus expected player for a slot.
 */
export function getExpectedPlayer(slot: number): string | undefined {
  return SLOT_ODDS[slot]?.expectedPlayer;
}

/**
 * Check if odds string indicates favorite.
 */
export function isOddsFavorite(odds: string): boolean {
  return odds.startsWith("-");
}
